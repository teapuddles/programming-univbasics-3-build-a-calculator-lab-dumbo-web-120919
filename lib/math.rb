def addition(num1, num2)
  sumadd = num1 + num2
  return sumadd
end

def subtraction(num1, num2)
  sumsub = num1 - num2
  return sumsub
end

def division(num1, num2)
  sumdiv = num1 / num2
  return sumdiv
end

def multiplication(num1, num2)
  sumpro = num1 * num2
  return sumpro
end

def modulo(num1, num2)
  summodulo = num1 % num2
  return summodulo
end

def square_root(num)
  sumsqrt = Math.sqrt(num)
  return = sumsqrt
end
